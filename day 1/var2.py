# 변수를 활용한 산술연산
# 청바지 수량
jean = 1
print(jean)
# 청바지를 추가 구매
jean = jean + 1
print("구매할 청바지 수량: ", jean, "벌")
#str()함수는 숫자를 문자형으로 바꿔줌 (형변환)
print("구매할 청바지 수량: "+ str(jean) + "벌")

계절 = '겨울' #한글이름변수
print(계절)
print(type(계절))

숫자 = '100'
print(type(숫자)) #str
#int()함수는 문자를 숫자형으로 변환함
print(int(숫자)+1) #101

# 비교 연산자 : > < ==
print(3 == 2) #False
print(3 > 2) #True
